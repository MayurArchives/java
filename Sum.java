import java.util.*;

public class Sum {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int x = sc.nextInt();
        int y = sc.nextInt();
        int sum = x + y;
        int difference = x - y;
        System.out.println(sum);
        System.out.println(difference);
        sc.close();

    }
}
