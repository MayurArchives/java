import java.util.*;

public class Patern3 {

    public static void main(String[] arg) {

        int r = 4;
        int c = 5;
        for (int i = 1; i <= r; i++) {
            for (int j = 1; j <= c; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }

}
